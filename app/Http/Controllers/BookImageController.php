    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public static function store(Request $request)
    {
        $imgPath = $request->file('file')->store('public/images');
        $image = new BookImage();
        $image->src = $imgPath;
        
        if (!($image->save())) {
            return false;
        } else {
            return $image->id;
        }
    }

